
CREATE TABLE COSTS (
    ID_COSTS INTEGER PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    ID_SHIP INTEGER REFERENCES SHIP(ID_SHIP) ON DELETE CASCADE,
    DATE DATE NOT NULL,
    ID_PORT INTEGER REFERENCES PORT(ID_PORT) ON DELETE SET NULL,
    ID_TYPE_COST INTEGER REFERENCES TYPE_COST(ID_TYPE_COST) ON DELETE SET NULL,
    VALUE NUMERIC NOT NULL,
    ID_CHARGE_DISCHARGE INTEGER REFERENCES CHARGE_DISCHARGE(ID_CHARGE_DISCHARGE) ON DELETE SET NULL
);
